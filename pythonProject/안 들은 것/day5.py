# 문제3 : 2~10단 까지 구구단 모두 출력
# for 중첩 [ for1 안에 for2 ] : for1 1번 반복될때마다 for2는 모두 반복실행
# for1 :
#   for2 :
for 단 in range( 2 , 11 ) : # 단 변수가 2 부터 10 까지 1씩 증가하면서 반복
    # 곱은 단수가 바뀔때마다 2~9 까지 반복실행
    for 곱 in range( 2 , 10 ) : # 곱 변수는 2부터 9까지 1씩 증가하면서 반복
        print( 단,"X",곱," = " ,단*곱)
