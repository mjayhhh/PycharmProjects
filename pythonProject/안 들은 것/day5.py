# 반복문 : while for

# while 논리 :    # 논리가 T[참이면] 반복실행  F[거짓이면] 반복실행X
#예1 ) 5번 반복 실행
반복 = 0      #
while 반복<5 : # 반복 변수가 5 미만이면 반복 실행
    print(" while 반복 실행중 " , 반복 )
    반복+=1   # 반복변수에 1 더하기
#예2) 1~100까지 누적 합계
합계 = 0
수 = 1
while 수 <=100 : # 수가 101 이면 반복문 종료
    합계+=수   # 합계에 수를 [1]더하고 합계에 [2]대입
    수+=1       # 수 변수에 1더하기
print(" 1~100까지 누적합 : ",합계) # 반복 종료후 결과

